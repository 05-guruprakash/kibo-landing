interface PixelCatProps {
  size?: number
  color?: string
  className?: string
}

export default function PixelCat({ size = 4, color = '#c8ff00', className = '' }: PixelCatProps) {
  const u = size
  const pixels: [number, number][] = [
    // Ears
    [2,0],[3,0],[11,0],[12,0],
    [1,1],[2,1],[3,1],[11,1],[12,1],[13,1],
    // Head
    [1,2],[2,2],[3,2],[4,2],[5,2],[6,2],[7,2],[8,2],[9,2],[10,2],[11,2],[12,2],[13,2],
    [1,3],[2,3],[3,3],[4,3],[5,3],[6,3],[7,3],[8,3],[9,3],[10,3],[11,3],[12,3],[13,3],
    // Eyes row (gaps = pupils)
    [1,4],[2,4],[5,4],[6,4],[7,4],[8,4],[9,4],[12,4],[13,4],
    [1,5],[2,5],[5,5],[6,5],[7,5],[8,5],[9,5],[12,5],[13,5],
    // Nose/mouth
    [2,6],[3,6],[4,6],[5,6],[6,6],[7,6],[8,6],[9,6],[10,6],[11,6],[12,6],
    [3,7],[5,7],[7,7],[9,7],[11,7],
    // Neck
    [4,8],[5,8],[6,8],[7,8],[8,8],[9,8],[10,8],
    // Body
    [2,9],[3,9],[4,9],[5,9],[6,9],[7,9],[8,9],[9,9],[10,9],[11,9],[12,9],
    [1,10],[2,10],[3,10],[4,10],[5,10],[6,10],[7,10],[8,10],[9,10],[10,10],[11,10],[12,10],[13,10],
    [1,11],[2,11],[3,11],[4,11],[5,11],[6,11],[7,11],[8,11],[9,11],[10,11],[11,11],[12,11],[13,11],
    [2,12],[3,12],[4,12],[5,12],[6,12],[7,12],[8,12],[9,12],[10,12],[11,12],[12,12],
    // Tail
    [13,10],[14,10],[14,11],[14,12],[13,12],
    // Legs
    [3,13],[4,13],[6,13],[7,13],[9,13],[10,13],
    [3,14],[4,14],[6,14],[7,14],[9,14],[10,14],
    // Paws
    [2,15],[3,15],[4,15],[5,15],[6,15],[7,15],[8,15],[9,15],[10,15],[11,15],
  ]

  const shadows = pixels.map(([c, r]) => `${c * u}px ${r * u}px 0 ${color}`).join(', ')

  return (
    <div className={className} style={{ position: 'relative', width: `${16 * u}px`, height: `${16 * u}px` }}>
      <div style={{ width: `${u}px`, height: `${u}px`, boxShadow: shadows, position: 'absolute', top: 0, left: 0 }} />
    </div>
  )
}
